//Em JavaScript, os operadores de atribuição são usados para atribuir valores a variáveis. Eles permitem que você atualize o valor de uma variável com base em uma operação específica. Abaixo estão alguns dos operadores de atribuição mais comuns em JavaScript:

//1. Operador de atribuição simples (=): Este operador atribui um valor à variável. Por exemplo:
   
   let x = 5; // Atribui o valor 5 à variável x
   

//2. Operador de atribuição com adição (+=): Este operador adiciona um valor à variável e atribui o resultado à própria variável. Por exemplo:
   
   let x1 = 10;
   x1 += 5; // Equivalente a x = x + 5; Resultado: x = 15
   

//3. Operador de atribuição com subtração (-=): Este operador subtrai um valor da variável e atribui o resultado à própria variável. Por exemplo:
   
   let x2 = 10;
   x2 -= 3; // Equivalente a x = x - 3; Resultado: x = 7
   

//4. Operador de atribuição com multiplicação (*=): Este operador multiplica um valor pela variável e atribui o resultado à própria variável. Por exemplo:
   
   let x3 = 5;
   x3 *= 3; // Equivalente a x = x * 3; Resultado: x = 15
   

//5. Operador de atribuição com divisão (/=): Este operador divide a variável por um valor e atribui o resultado à própria variável. Por exemplo:
   
   let x4 = 10;
   x4 /= 2; // Equivalente a x = x / 2; Resultado: x = 5
   

//6. Operador de atribuição com resto (%=): Este operador calcula o resto da divisão da variável por um valor e atribui o resultado à própria variável. Por exemplo:
   
   let x5 = 10;
   x5 %= 3; // Equivalente a x = x % 3; Resultado: x = 1
   

