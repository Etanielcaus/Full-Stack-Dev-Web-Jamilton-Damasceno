//!Funções Nativas - Matematica

let numero = 280.50

// Valor de pi
console.log(Math.PI); // retorna 3.141592653589793

// Valor absoluto
console.log(Math.abs(-5)); // retorna 5

// Arredondamento para o número inteiro mais próximo
console.log(Math.round(4.6)); // retorna 5

// Arredondamento para o próximo número inteiro menor ou igual
console.log(Math.floor(4.9)); // retorna 4

// Arredondamento para o próximo número inteiro maior ou igual
console.log(Math.ceil(4.2)); // retorna 5

// Valor máximo
console.log(Math.max(2, 4, 6, 8)); // retorna 8

// Valor mínimo
console.log(Math.min(2, 4, 6, 8)); // retorna 2

// Potência
console.log(Math.pow(2, 3)); // retorna 8

// Raiz quadrada
console.log(Math.sqrt(25)); // retorna 5

// Número aleatório entre 0 e 1 (para escokher um numero aleatorio maior, basta multiplocar por mais (* 200))
console.log(Math.random());





